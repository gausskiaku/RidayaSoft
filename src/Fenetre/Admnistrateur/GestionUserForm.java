    public void allUser() {
        try {
            List<User> listUser = new UserMetier().allUser(em);
            for (User user : listUser) {
                model.insertRow(model.getRowCount(), new Object[]{user.getIdUser(), user.getLogin(), user.getMotDePasse(), user.getDroit()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

