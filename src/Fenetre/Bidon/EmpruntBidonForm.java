    public void cbClient() {
        Query qrClient = em.createNamedQuery("Client.findAll");
        List<Client> listClient = qrClient.getResultList();
        cbClientNom.removeAllItems();

        for (Client client : listClient) {
            cbClientNom.addItem(client.getNomClient());
        }
        cbClientNom.setSelectedIndex(-1);
    }

    public void cbTypeBidon() {
        Query qrBidon = em.createNamedQuery("Bidon.findAll");
        List<Bidon> listBidon = qrBidon.getResultList();
        cbTypeBidonEmprunt.removeAllItems();
        for (Bidon bidon : listBidon) {
            cbTypeBidonEmprunt.addItem(bidon.getTypeBidon());
        }
        cbTypeBidonEmprunt.setSelectedIndex(-1);
    }

