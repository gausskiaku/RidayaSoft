    private void ajoutPanier() {
        try {
            if (Integer.parseInt(txtQtBidonEmprunter.getText()) > Integer.parseInt(txtQtDispoTypeBidon.getText())) {
                JOptionPane.showMessageDialog(null, "La quantité commandée est superieure à la quatité disponible dans le stock !", "RidayaSoft : Emprunt Bidon", JOptionPane.ERROR_MESSAGE);

            } else if (cbClientNom.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(null, "Verifier le nom du client !", "RidayaSoft : Emprunt bidon", JOptionPane.ERROR_MESSAGE);

            } else if (cbTypeBidonEmprunt.getSelectedIndex() == -1 || cbTypeBidonEmprunt.getSelectedItem().toString().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Veuillez choisir le type de Bidon !", "RidayaSoft : Emprunt bidon", JOptionPane.ERROR_MESSAGE);
            } else if (Integer.parseInt(txtQtBidonEmprunter.getText()) < 1) {
                JOptionPane.showMessageDialog(null, "Veuillez introduire la quantité existante !", "RidayaSoft : Emprunt bidon", JOptionPane.ERROR_MESSAGE);
            } else {
                Emprunter emprunter = new Emprunter();
                emprunter.setDateEmprunter(Date.from(Instant.now()));
                emprunter.setQuantiteEmprunter(Integer.parseInt(txtQtBidonEmprunter.getText()));
                emprunter.setIdBidon(idBidon);
                emprunter.setIdClient(idClient);

                listEmprunt.add(emprunter);

                // Table
                model.insertRow(model.getRowCount(), new Object[]{listEmprunt.size(), idBidon.getIdBidon(),
                    idBidon.getTypeBidon(), Integer.parseInt(txtQtBidonEmprunter.getText())});

                cbTypeBidonEmprunt.setSelectedIndex(-1);
                txtQtBidonEmprunter.setText("");
                txtQtDispoTypeBidon.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erreur : Bidon non ajouté au panier ! ", "Ridaya Soft : Emprunt Bidon", JOptionPane.ERROR_MESSAGE);
        }
    }
