    private void ajoutPanier() {
        //  JOptionPane.showMessageDialog(null, idBidon.getIdBidon());
        try {

            if (Integer.parseInt(txtQtBidonRemise.getText()) > Integer.parseInt(txtQtDevoirTypeBidon.getText())) {
                JOptionPane.showMessageDialog(null, "La quantité remise est superieure à la quatité emprunter !", "RidayaSoft : Remise Bidon", JOptionPane.ERROR_MESSAGE);
            } else if (cbClientNom.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(null, "Verifier le nom du client !", "RidayaSoft : Remise bidon", JOptionPane.ERROR_MESSAGE);
            } else if (cbTypeBidonRemise.getSelectedIndex() == -1 || cbTypeBidonRemise.getSelectedItem().toString().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Veuillez choisir le type de Bidon !", "RidayaSoft : Emprunt bidon", JOptionPane.ERROR_MESSAGE);
            } else if (Integer.parseInt(txtQtBidonRemise.getText()) < 1) {
                JOptionPane.showMessageDialog(null, "Veuillez introduire la quantité existante !", "RidayaSoft : Emprunt bidon", JOptionPane.ERROR_MESSAGE);
            } else {

                Remettre remettre = new Remettre();
                remettre.setDateRemettre(Date.from(Instant.now()));
                remettre.setQuantiteRemettre(Integer.parseInt(txtQtBidonRemise.getText()));
                remettre.setIdBidon(idBidon);
                remettre.setIdClient(idClient);
                listRemettre.add(remettre);

                // Table
                model.insertRow(model.getRowCount(), new Object[]{listRemettre.size(), idBidon.getIdBidon(),
                    idBidon.getTypeBidon(), Integer.parseInt(txtQtBidonRemise.getText())});

                cbTypeBidonRemise.setSelectedIndex(-1);
                txtQtBidonRemise.setText("");
                txtQtDevoirTypeBidon.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erreur : Bidon non ajouté au panier ! \n Erreur : " + e.getMessage(), "Ridaya Soft : Remise Bidon", JOptionPane.ERROR_MESSAGE);
        }
    }
