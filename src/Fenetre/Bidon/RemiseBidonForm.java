    public void cbClient() {
        List listClient = new ClientMetier().clientConcernerParUneRemise(em);
        cbClientNom.removeAllItems();

        for (Object client : listClient) {
            Object[] clientTab = (Object[]) client;
            cbClientNom.addItem(clientTab[11]);
        }
        cbClientNom.setSelectedIndex(-1);
    }

    public void cbTypeBidon(String nomClient) {
        List listBidon = new ClientMetier().clientConcernerParUneRemiseParNom(em, nomClient);
        cbTypeBidonRemise.removeAllItems();
        for (Object bidon : listBidon) {
            Object[] bidonTab = (Object[]) bidon;
            cbTypeBidonRemise.addItem(bidonTab[14]);
        }
        cbTypeBidonRemise.setSelectedIndex(-1);
    }

