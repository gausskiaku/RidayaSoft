    private void btStockBidonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btStockBidonSaveActionPerformed
        try {
//            double quantiteTotal = 0;
////            quantiteTotal = quantiteTotal + 
//            for (int i = 0; i < tbRemiseBidon.getRowCount(); i++) {
//                quantiteTotal = Double.parseDouble(model.getValueAt(i, 3).toString());
//            }
//            JOptionPane.showMessageDialog(null, " : " + quantiteTotal, "RidayaSoft : Remise bidon", JOptionPane.ERROR_MESSAGE);

            if (listRemettre.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Aucune remise n'a été faite ! ", "RidayaSoft : Remise bidon", JOptionPane.ERROR_MESSAGE);
            } else if (cbClientNom.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(null, "Verifier le nom du client !", "RidayaSoft : Remise bidon", JOptionPane.ERROR_MESSAGE);
            } 
//                else if (quantiteTotal > Double.parseDouble(txtQtDevoirTypeBidon.getText())) {
//                JOptionPane.showMessageDialog(null, "Verifier le panier (Tableau). La quantité remise semble superieur à la quantité empruntée !", "RidayaSoft : Remise bidon", JOptionPane.ERROR_MESSAGE);
//            } 
            else {

                try {
                    EntityTransaction transaction = em.getTransaction();
                    for (Remettre onlyRemettre : listRemettre) {
//                        Remettre remettreAEnregistrer = new Remettre();
//                        remettreAEnregistrer.setIdBidon(onlyRemettre.getIdBidon());
//                        remettreAEnregistrer.setIdClient(client);
//                        remettreAEnregistrer.setDateRemettre(onlyRemettre.getDateRemettre());
//                        remettreAEnregistrer.setQuantiteRemettre(onlyRemettre.getQuantiteRemettre());

                        transaction.begin();
                        em.persist(onlyRemettre);
                        transaction.commit();
                        nombreEmprunt++;
                    }

                    JOptionPane.showMessageDialog(null, "La remise du client " + cbClientNom.getSelectedItem().toString().toUpperCase() + " a été enregistré!", "RidayaSoft : Remise Bidon", JOptionPane.INFORMATION_MESSAGE);

                    new Fichier().ecrireFichierLogAccess("Enregistrement de la remise du client " + cbClientNom.getSelectedItem().toString().toUpperCase() + " à " + Date.from(Instant.now()));

                    init();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Deux ou plusieurs produits du même nom sur le panier : Sommer les produits ayant le même nom !");
                    init();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erreur, veuillez contacter l'administrateur au 0814444084 ! \n " + e.getMessage());
        }
    }//GEN-LAST:event_btStockBidonSaveActionPerformed
    public void init() {
        cbClientNom.setSelectedIndex(-1);
        cbTypeBidonRemise.setSelectedIndex(-1);
        txtQtDevoirTypeBidon.setText("");
        txtQtBidonRemise.setText("");
        listRemettre.clear();
        for (int i = model.getRowCount() - 1; i > -1; i--) {
            model.removeRow(i);
        }
    }
