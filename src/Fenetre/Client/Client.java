    public void saveClient() {
        try {

            if (txtClientNom.getText().length() < 3) {
                JOptionPane.showMessageDialog(null, "Veuillez introduire un nom existant !", "Ridaya Soft : Client", JOptionPane.ERROR_MESSAGE);
            } else {
                Entity.Client client = new Entity.Client();
                client.setNomClient(txtClientNom.getText());

                EntityTransaction transaction = em.getTransaction();
                transaction.begin();
                em.persist(client);
                transaction.commit();
                JOptionPane.showMessageDialog(null, "Client ajouté avec succes !", "Ridaya Soft : Famille", JOptionPane.INFORMATION_MESSAGE);
                new Fichier().ecrireFichierLogAccess("Ajout de la famille " + txtClientNom.getText() + " à " + Date.from(Instant.now()));
                this.dispose();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erreur : Verifier si le client n'existe pas. \nOu contact l'Administrateur au 0814444084 !", "Ridaya Soft : Famille", JOptionPane.INFORMATION_MESSAGE);

        }
    }

