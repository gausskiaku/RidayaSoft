    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            
        
        if(dtClotureJour.getDate().toString().isEmpty()){
        JOptionPane.showMessageDialog(null, "Pas de date choisi ! ", "RidayaSoft : Clôture journalière", JOptionPane.ERROR_MESSAGE);
        } else {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String date = sdf.format(dtClotureJour.getDate());
        dateJour = date;
        if(model.getRowCount() > 0){
            for (int i = model.getRowCount() - 1; i > -1; i--) {
                model.removeRow(i);
            }
        }
        venteJournaliere(date);
      }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Date inconnu : Verifie la date de recherche", "RidayaSoft : Clôture journalière", JOptionPane.ERROR_MESSAGE);
        venteJournaliere(metier.dateCourante());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

