    public void venteJournaliere(String date) {
        try {
            List listFacture = stockMetier.venteJournaliere(em, date);

            for (Object facture : listFacture) {
                Object[] factureTab = (Object[]) facture;
                model.insertRow(model.getRowCount(), new Object[]{factureTab[0], factureTab[1], factureTab[2], factureTab[3]});

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

