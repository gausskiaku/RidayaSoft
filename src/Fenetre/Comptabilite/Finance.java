    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String lienReport = getClass().getResource("/Report/finalReport.jrxml").toString().replace("file:/", "");
        String sql = "SELECT facture.idFacture, client.nomClient, SUM(commander.prixVenteCommande * commander.quantiteCommande) AS Montant, facture.dateFacture, (coalesce((SELECT SUM(depense.montant) FROM depense WHERE dateDepense LIKE '" + dateJour + "%'),0)) AS montantDepense\n"
                + "FROM facture\n"
                + "INNER JOIN client ON facture.idClient = client.idClient\n"
                + "INNER JOIN commander ON commander.idClient = client.idClient\n"
                + "INNER JOIN produit ON produit.idProduit = commander.idProduit\n"
                + "WHERE facture.dateFacture LIKE '" + dateJour + "%'";

        int printFacture = JOptionPane.showConfirmDialog(null, "Voulez vous imprimer la vente journalière?", "RidayaSoft : Vente Journalière", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (printFacture == JOptionPane.YES_OPTION) {
            try {
                new IReport().reportDynamique(lienReport, sql);
                  new Fichier().ecrireFichierLogAccess("Impression finance du " + dateJour + " à " + Date.from(Instant.now()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erreur rapport : " + e);
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

