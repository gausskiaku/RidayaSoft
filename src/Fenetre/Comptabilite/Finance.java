    public void reportFinal(String date) {
        try {
            List list = new DepenseMetier().reportJournalier(em, date);

            for (Object res : list) {
                Object[] resTab = (Object[]) res;
                model.insertRow(model.getRowCount(), new Object[]{model.getRowCount(), resTab[2], resTab[4], resTab[3], (Double.parseDouble(resTab[2].toString()) - Double.parseDouble(resTab[4].toString()))});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Aucune vente pour cette journée !");
        }
    }
    
    public void reportFinalByMonth(String date){
        try {
            List list = new DepenseMetier().reportMensuel(em, date);
            
            for (Object res : list) {
                Object[] resTab = (Object[]) res;
                model.insertRow(model.getRowCount(), new Object[]{model.getRowCount(), resTab[2], resTab[4], dt.getDate().getMonth() + 1, (Double.parseDouble(resTab[2].toString()) - Double.parseDouble(resTab[4].toString()))});
            }
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Aucun resultat pour ce mois!");
        }
    
    
    }

