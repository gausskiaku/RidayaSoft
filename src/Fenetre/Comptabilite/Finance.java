    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       try {
            if (dt.getDate().toString().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Pas de date choisi ! ", "RidayaSoft : Rapport final", JOptionPane.ERROR_MESSAGE);
            } else {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String date = sdf.format(dt.getDate());
                dateJour = date;
                if (model.getRowCount() > 0) {
                    for (int i = model.getRowCount() - 1; i > -1; i--) {
                        model.removeRow(i);
                    }
                }
                reportFinal(dateJour);
                  new Fichier().ecrireFichierLogAccess("Recherche finance du " + dateJour + " à " + Date.from(Instant.now()));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Date inconnu : Verifie la date de recherche", "RidayaSoft : Entrée stock", JOptionPane.ERROR_MESSAGE);
           
        }
    }//GEN-LAST:event_jButton2ActionPerformed

