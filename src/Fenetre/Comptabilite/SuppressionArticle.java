    public void articlesClients(String date, int id) {
        try {
            List listArticles = new ArticleMetier().getArticles(em, date, id);

            for (Object article : listArticles) {
                Object[] articleTab = (Object[]) article;
                model.insertRow(model.getRowCount(), new Object[]{articleTab[14], articleTab[8], (int) articleTab[8] * (double) articleTab[9], articleTab[12], articleTab[11], articleTab[8]});

                txtNomClient.setText(String.valueOf(articleTab[6]));
                txtDateCreation.setText(String.valueOf(articleTab[1]));

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

