    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        int idProduit = Integer.parseInt((model.getValueAt(tbArticle.getSelectedRow(), 4)).toString());
        int idClient = Integer.parseInt((model.getValueAt(tbArticle.getSelectedRow(), 3)).toString());
        int idCommande = Integer.parseInt((model.getValueAt(tbArticle.getSelectedRow(), 5)).toString()); // articleTab[8]

//        CommanderJpaController commanderJpaController = new CommanderJpaController(emf);
        Commander commander = em.find(Commander.class, idCommande);

        try {
//            commanderJpaController.destroy(commanderPK);
            EntityTransaction transaction = em.getTransaction();
            transaction.begin();
            em.remove(commander);
            transaction.commit();
            JOptionPane.showMessageDialog(null, "L'article " + (String) model.getValueAt(tbArticle.getSelectedRow(), 0) + " a été supprimer de la commande!", "RidayaSoft : Suppression Article", JOptionPane.INFORMATION_MESSAGE);
            new Fichier().ecrireFichierLogAccess("Suppression de l'article "+(String) model.getValueAt(tbArticle.getSelectedRow(), 0)+" du client " + txtNomClient.getText() + " de la facture " + txtNumFacture.getText() + " à " + Date.from(Instant.now()));
            model.removeRow(tbArticle.getSelectedRow());
            
            int printFacture = JOptionPane.showConfirmDialog(null, "Voulez vous fermer ?", "RidayaSoft : Suppression article", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (printFacture == JOptionPane.YES_OPTION) {
                this.dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Pas de suppression car " + e.getMessage());
        }


    }//GEN-LAST:event_jButton3ActionPerformed

