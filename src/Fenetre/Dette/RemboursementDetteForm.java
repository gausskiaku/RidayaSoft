    private void btStockBidonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btStockBidonSaveActionPerformed
        try {
            if (cbNomClient.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(null, "Verifier le nom du client !", "RidayaSoft : Remise bidon", JOptionPane.ERROR_MESSAGE);
                
            } else if (Double.parseDouble(txtMontantPayerDette.getText()) > Double.parseDouble(txtMontantTotalRemboursement.getText())) {
                JOptionPane.showMessageDialog(null, "Le montant payé ne doit pas être superieur au montant emprunter !", "RidayaSoft : Remboursement Dette", JOptionPane.ERROR_MESSAGE);
                
            } else if (txtMontantPayerDette.getText().length() < 3) {
                JOptionPane.showMessageDialog(null, "Verifier le montant !", "RidayaSoft : Remboursement Dette", JOptionPane.ERROR_MESSAGE);
        } else {
                
                EntityTransaction transaction = em.getTransaction();
                
                try {
                    
                    Remboursement remboursement = new Remboursement();
                    remboursement.setIdClient(idClient);
                    remboursement.setDateRemboursement(Date.from(Instant.now()));
                    remboursement.setDeviseRemboursement("CDF");
                    remboursement.setMontantRemboursement(Double.parseDouble(txtMontantPayerDette.getText()));
                    
                    transaction.begin();
                    em.persist(remboursement);
                    transaction.commit();
                    JOptionPane.showMessageDialog(null, "Paiement validé !", "Ridaya Soft : Dette Remboursement", JOptionPane.INFORMATION_MESSAGE);
                    new Fichier().ecrireFichierLogAccess("Validation du remboursement dette de " + cbNomClient.getSelectedItem() + " à " + Date.from(Instant.now()) + " Montant : " + txtMontantPayerDette.getText() + " FC");
                    
                    init();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erreur, contacte l'Administrateur au 814444084 !\n \n" + e.getMessage());
                    init();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erreur, contacte l'Administrateur au 814444084 !\n \n" + e.getMessage());
        }
    }//GEN-LAST:event_btStockBidonSaveActionPerformed
