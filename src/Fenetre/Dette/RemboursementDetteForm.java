    public void cbClient() {
        List listClient = new DetteMetier().clientInsovable(em);
        cbNomClient.removeAllItems();
        
        for (Object client : listClient) {
            Object[] clientTab = (Object[]) client;
            if (Double.parseDouble(clientTab[0].toString()) > 0) { // Condition : Juste avoir ceux qui doivent rembourser
                cbNomClient.addItem(clientTab[1]);
            }
        }
        cbNomClient.setSelectedIndex(-1);
    }

