    public void listeProduit() {
        try {
            List<Produit> listProduit = produitMetier.allProduit(em);

            for (Produit produit : listProduit) {
                model.insertRow(model.getRowCount(), new Object[]{produit.getIdProduit(), produit.getNomProduit(), produit.getDateCreation(), produit.getPrixUnitaire(), produit.getSeuil()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

