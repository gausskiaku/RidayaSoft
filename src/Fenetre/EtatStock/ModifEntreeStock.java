    private void getInfoStock() {
        try {
            Query qr = em.createNamedQuery("Stock.findByIdStock");
            qr.setParameter("idStock", Integer.parseInt(txtIdProduitModif.getText().trim()));
            List<Stock> listStock = qr.getResultList();
            Stock stock = listStock.get(0);
            txtPrixProduitModif.setText(String.valueOf(stock.getPrixAchat()));
            txtQuantiteProduitModif.setText(String.valueOf(stock.getQuantite()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Info : " + e.getMessage());
        }

    }

