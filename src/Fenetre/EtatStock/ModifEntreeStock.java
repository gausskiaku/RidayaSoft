    private void btModifProduitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btModifProduitActionPerformed
        Stock stock = em.find(Stock.class, Integer.parseInt(txtIdProduitModif.getText()));
        stock.setPrixAchat(Double.parseDouble(txtPrixProduitModif.getText()));
        stock.setQuantite(Integer.parseInt(txtQuantiteProduitModif.getText()));
        
        try {
            EntityTransaction transaction = em.getTransaction();
            transaction.begin();
            em.merge(stock);
            transaction.commit();
            JOptionPane.showMessageDialog(null, "Le stock a été modifié avec succes !", "Ridaya Soft : Modif Stock", JOptionPane.INFORMATION_MESSAGE);
            new Fichier().ecrireFichierLogAccess("Modification du stock "+txtIdProduitModif.getText() + " à partir du " + Date.from(Instant.now()));
            int printFacture = JOptionPane.showConfirmDialog(null, "Voulez vous fermer ?", "RidayaSoft : Modification stock", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (printFacture == JOptionPane.YES_OPTION) {
                this.dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Pas de modification car " + e.getMessage());
        }
    }//GEN-LAST:event_btModifProduitActionPerformed

