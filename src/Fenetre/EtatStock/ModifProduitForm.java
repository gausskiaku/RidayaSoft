    private void btModifProduitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btModifProduitActionPerformed
        Produit produit = em.find(Produit.class, Integer.parseInt(txtIdProduit.getText()));
        produit.setNomProduit(txtNomProduit.getText());
        produit.setPrixUnitaire(Double.parseDouble(txtPrixProduit.getText()));
        produit.setSeuil(Integer.parseInt(txtSeuilProduit.getText()));

        try {
            EntityTransaction transaction = em.getTransaction();
            transaction.begin();
            em.merge(produit);
            transaction.commit();
            JOptionPane.showMessageDialog(null, "Produit " + txtNomProduit.getText() + " a √©t√© modifi√© avec succes !", "Ridaya Soft : Modif Produit", JOptionPane.INFORMATION_MESSAGE);
             new Fichier().ecrireFichierLogAccess("Modification du produit "+txtNomProduit.getText()+" le prix est maintenant √† "+ txtPrixProduit.getText() + " √† partir du " + Date.from(Instant.now()));
            int printFacture = JOptionPane.showConfirmDialog(null, "Voulez vous fermer ?", "RidayaSoft : Suppression article", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (printFacture == JOptionPane.YES_OPTION) {
                this.dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Pas de modification car " + e.getMessage());
        }

    }//GEN-LAST:event_btModifProduitActionPerformed

