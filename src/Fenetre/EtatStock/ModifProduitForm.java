    private void getInfoProduit() {
        try {
            Query qr = em.createNamedQuery("Produit.findByIdProduit");
            qr.setParameter("idProduit", Integer.parseInt(txtIdProduit.getText().trim()));
            List<Produit> listProduit = qr.getResultList();
            Produit produit = listProduit.get(0);
            txtNomProduit.setText(produit.getNomProduit());
            txtPrixProduit.setText(String.valueOf(produit.getPrixUnitaire()));
            txtSeuilProduit.setText(String.valueOf(produit.getSeuil()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Info : " + e.getMessage());
        }

    }

