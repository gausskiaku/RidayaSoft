    private void btPrintSortieSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPrintSortieSearchActionPerformed
        try {
            String sql = "SELECT produit.idProduit, produit.nomProduit, SUM( commander.quantiteCommande ) AS quantite, commander.prixVenteCommande, facture.dateFacture\n"
                + "FROM produit\n"
                + "INNER JOIN commander ON produit.idProduit = commander.idProduit\n"
                + "INNER JOIN client ON client.idClient = commander.IdClient\n"
                + "INNER JOIN facture ON facture.idClient = client.idClient\n"
                + "WHERE facture.dateFacture BETWEEN '"+dateStart+" 00:00:00' AND '"+dateEnd+" 23:59:00'\n"
                + "GROUP BY produit.idProduit, produit.nomProduit, commander.prixVenteCommande, facture.dateFacture\n"
                + "ORDER BY facture.dateFacture";
        
        new IReport().reportDynamique("C:\\Users\\Gauss\\Documents\\NetBeansProjects\\RidayaSoft\\Rapport\\SortieStockReport.jrxml", sql);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_btPrintSortieSearchActionPerformed

