    private void btPrintEntree2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPrintEntree2ActionPerformed
        try {
            if (dtJourEntree.getDate().toString().isEmpty() || dtJourSortie.getDate().toString().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Pas de date choisi ! ", "RidayaSoft : Sortie stock", JOptionPane.ERROR_MESSAGE);
            } else if (dtJourEntree.getDate().after(dtJourSortie.getDate())) {
                JOptionPane.showMessageDialog(null, "La date start vient après la date end ", "RidayaSoft : Sortie stock", JOptionPane.ERROR_MESSAGE);
            } else {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                dateStart = sdf.format(dtJourEntree.getDate());
//                dateJour = date;

                dateEnd = sdf.format(dtJourSortie.getDate());

                if (model.getRowCount() > 0) {
                    for (int i = model.getRowCount() - 1; i > -1; i--) {
                        model.removeRow(i);
                    }
                }
                listeStockByDate(dateStart, dateEnd);
                btPrintSortieSearch.setEnabled(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Date inconnu : Verifie la date de recherche", "RidayaSoft : Entrée stock", JOptionPane.ERROR_MESSAGE);
            // listeStock();
        }
    }//GEN-LAST:event_btPrintEntree2ActionPerformed

