    public void listeStock() {
        try {
            List listStock = stockMetier.sortieStock(em);

            for (Object produit : listStock) {
                Object[] produitTab = (Object[]) produit;
                model.insertRow(model.getRowCount(), new Object[]{produitTab[0], produitTab[1], produitTab[2], produitTab[3], produitTab[4]});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
