    public void saveFamille() {
        if (txtFamilleDes.getText().length() < 3) {
            JOptionPane.showMessageDialog(null, "Veuillez introduire un nom existant !", "Ridaya Soft : Famille", JOptionPane.ERROR_MESSAGE);
        } else {
            Famille famille = new Famille();
            famille.setDesignation(txtFamilleDes.getText());

            EntityTransaction transaction = em.getTransaction();
            transaction.begin();
            em.persist(famille);
            transaction.commit();
            JOptionPane.showMessageDialog(null, "Famille ajouter avec succes !", "Ridaya Soft : Famille", JOptionPane.INFORMATION_MESSAGE);
            new Fichier().ecrireFichierLogAccess("Ajout de la famille " + txtFamilleDes.getText() + " √† " + Date.from(Instant.now()));
            txtFamilleDes.setText("");

            new Metier().supTab(model);
            allFamille();
        }
    }

