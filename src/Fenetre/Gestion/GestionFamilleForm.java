    public void allFamille() {
        try {
            List<Famille> listFamille = familleMetier.allFamille(em);

            for (Famille famille : listFamille) {
                model.insertRow(model.getRowCount(), new Object[]{famille.getIdFamille(), famille.getDesignation()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

