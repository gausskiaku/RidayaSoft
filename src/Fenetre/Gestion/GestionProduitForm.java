    public void cbFamille() {
        Query qrFamille = em.createNamedQuery("Famille.findAll");
        List<Famille> listFamille = qrFamille.getResultList();
        cboFamille.removeAllItems();

        for (Famille famille : listFamille) {
            cboFamille.addItem(famille.getDesignation());
        }
        cboFamille.setSelectedIndex(-1);

    }

