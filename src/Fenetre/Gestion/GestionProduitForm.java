    private void btProduitSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btProduitSaveActionPerformed
        try {
            
        
        if (cboFamille.getSelectedIndex() == -1 || txtProduitNom.getText().length() < 3 || txtProduitPrixUnitaire.getText().length() < 3) {
            JOptionPane.showMessageDialog(null, "Veuillez remplir les champs vide !", "Ridaya Soft : Produit", JOptionPane.ERROR_MESSAGE);
        } else {
            Produit produit = new Produit();
            if (txtProduitSeuil.getText().isEmpty()) {
                produit.setSeuil(0);
            } else {
                produit.setSeuil(Integer.parseInt(txtProduitSeuil.getText()));
            }

            produit.setDateCreation(Date.from(Instant.now()));
            produit.setIdFamille(idFamille);
            produit.setNomProduit(txtProduitNom.getText());
            produit.setPrixUnitaire(Double.parseDouble(txtProduitPrixUnitaire.getText()));

            EntityTransaction transaction = em.getTransaction();
            transaction.begin();
            em.persist(produit);
            transaction.commit();
            new Fichier().ecrireFichierLogAccess("Ajout du produit " + txtProduitNom.getText() + " Ã  " + Date.from(Instant.now()));
            JOptionPane.showMessageDialog(null, "Produit ajouter avec succes !", "Ridaya Soft : Produit", JOptionPane.INFORMATION_MESSAGE);
              
            init();
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btProduitSaveActionPerformed
