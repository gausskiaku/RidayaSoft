    public void allTypeVente() {
        try {
            List<Typevente> listTypeVente = new TypeVenteMetier().allTypeVenete(em);

            for (Typevente typevente : listTypeVente) {
                model.insertRow(model.getRowCount(), new Object[]{typevente.getIdType(), typevente.getDesignation()});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

