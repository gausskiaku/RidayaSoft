public void  saveTypeVente(){
   if (txtTypeVenteDes.getText().length() < 4) {
            JOptionPane.showMessageDialog(null, "Veuillez introduire un nom existant !", "Ridaya Soft : Type Vente", JOptionPane.ERROR_MESSAGE);
        } else {
            Typevente typeVente = new Typevente();
            typeVente.setDesignation(txtTypeVenteDes.getText());

            EntityTransaction transaction = em.getTransaction();
            transaction.begin();
            em.persist(typeVente);
            transaction.commit();
            JOptionPane.showMessageDialog(null, "Type Vente ajouter avec succes !", "Ridaya Soft : Type Vente", JOptionPane.INFORMATION_MESSAGE);
            new Fichier().ecrireFichierLogAccess("Ajout du type vente à " + Date.from(Instant.now()));
            txtTypeVenteDes.setText("");
            new Metier().supTab(model);
            allTypeVente();
            
        }
}

