    private void ajoutPanier() {
        try {
            
            if (Integer.parseInt(txtCommandeQuantite.getText()) > Integer.parseInt(txtEtatStock.getText())) {
                JOptionPane.showMessageDialog(null, "La quantitÃ© commandÃ© est superieure Ã  la quatitÃ© disponible dans le stock !", "RidayaSoft : Facturation", JOptionPane.ERROR_MESSAGE);

            } else if (cbProduit.getSelectedIndex() == -1 || cbProduit.getSelectedItem().toString().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Veuillez choisir le produit !", "RidayaSoft : Facturation", JOptionPane.ERROR_MESSAGE);
            } else if (Integer.parseInt(txtCommandeQuantite.getText()) < 1) {
                JOptionPane.showMessageDialog(null, "Veuillez introduire la quantitÃ© existante !", "RidayaSoft : Facturation", JOptionPane.ERROR_MESSAGE);
            } 
            else if ( Double.parseDouble(txtRemise.getText()) > Double.parseDouble(txtPrixTotal.getText())) {
                JOptionPane.showMessageDialog(null, "La remise est superieur au montant payÃ© !", "RidayaSoft : Facturation", JOptionPane.ERROR_MESSAGE);
            } 
            else {
                Commander commande = new Commander();
                commande.setDateCommander(Date.from(Instant.now()));
                commande.setPrixVenteCommander(Double.parseDouble(txtPrixVente.getText()));
                commande.setIdProduit(idProduit);
                commande.setQuantiteCommander(Integer.parseInt(txtCommandeQuantite.getText()));
                commande.setRemiseCommander(Double.parseDouble(txtRemise.getText()));
                listCommande.add(commande);

                // Table
                model.insertRow(model.getRowCount(), new Object[]{
                    listCommande.size(),
                    idProduit.getIdProduit(),
                    idProduit.getNomProduit(),
                    Integer.parseInt(txtCommandeQuantite.getText()),
                    idProduit.getPrixUnitaire(),
                    (Integer.parseInt(txtCommandeQuantite.getText()) * idProduit.getPrixUnitaire()),
                    Integer.parseInt(txtRemise.getText()),
                   ((Integer.parseInt(txtCommandeQuantite.getText()) * idProduit.getPrixUnitaire()) - Integer.parseInt(txtRemise.getText()))});

                netApayerTotal = netApayerTotal + Double.parseDouble(txtNetAPayer.getText());

                cbProduit.setSelectedIndex(-1);
                txtCommandeQuantite.setText("");
                txtEtatStock.setText("");
                txtNetAPayer.setText("");
                txtPrixTotal.setText("");
                txtRemise.setText("");
                txtPrixVente.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erreur : Produit non ajouter au panier ! " + e.getMessage(), "Ridaya Soft : Vente", JOptionPane.ERROR_MESSAGE);
        }
    }
